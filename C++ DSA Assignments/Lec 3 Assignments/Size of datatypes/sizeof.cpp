#include<iostream>
using namespace std;
int main()
{
    cout<<"\n Size of basic data types : \n";
    cout<<"\n Size of char : "<<sizeof(char)<<" bytes";
    cout<<"\n Size of short : "<<sizeof(short)<<" bytes";
    cout<<"\n Size of int : "<<sizeof(int)<<" bytes";
    cout<<"\n Size of long : "<<sizeof(long)<<" bytes";
    cout<<"\n Size of long long : "<<sizeof(long long)<<" bytes";
    cout<<"\n Size of float : "<<sizeof(float)<<" bytes";
    cout<<"\n Size of double : "<<sizeof(double)<<" bytes";
    cout<<"\n Size of long double : "<<sizeof(long double)<<" bytes";
    cout<<"\n Size of bool : "<<sizeof(bool)<<" bytes";
    return 0;
}